export interface EmissionFactorInput {
  name: string;
  category: string;
  unit: string;
  co2PerUnit: number;
  status?: EmissionFactorStatus;
}

export interface ProductESGProfileInput {
  productName: string;
  productCode: string;
  category?: string;
  carbonFootprint?: number;
  sustainableFlag?: boolean;
  notes?: string;
  status?: EmissionFactorStatus;
}

export interface CarbonTransactionInput {
  departmentId: string;
  emissionFactorId: string;
  activityAmount: number;
  sourceType?: CarbonSourceType;
  sourceRef?: string;
  transactionDate?: string;
}

export interface EnvironmentalGoalInput {
  name: string;
  departmentId: string;
  targetCo2Kg: number;
  currentCo2Kg?: number;
  deadline: string; // ISO
  status?: GoalStatus;
}